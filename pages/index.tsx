import type { NextPage } from 'next'
import Head from 'next/head'
import { useState, useEffect, useRef } from 'react'
import d3ToPng from 'd3-svg-to-png'
import { getAccount, watchAccount, writeContract } from '@wagmi/core'
import { useAppSelector } from 'store'
import { AppShell, Navbar, Header, Text } from '@mantine/core'
import { Group, Button, Box, Modal, Title } from '@mantine/core'
import { ScrollArea, AspectRatio, Center } from '@mantine/core'
import { ConnectButton } from '@rainbow-me/rainbowkit'
import ToolStack from 'components/ToolStack'
import SvgItem from 'components/SvgItem'
import { uploadImage } from 'lib/nftStorage'
import { prepareSafeMint } from 'lib/nftContract'
import { setAttributes, setMetadata } from 'lib/metadata'

type Props = {}

const Home: NextPage<Props> = () => {
  const { bgColor, country, year, ch } = useAppSelector((state) => state.editor)
  const [open, setOpen] = useState(false)
  const [account, setAccount] = useState(getAccount().address)
  const [wait, setWait] = useState(false) // async
  const svgRef = useRef('')

  useEffect(() => {
    // watch
    const unwatch = watchAccount(({ address }) => {
      if (address) {
        setAccount(address)
        console.log(`Connect with ${address}`)
      }
    })
    return unwatch
  }, [])

  const handleMint = async () => {
    try {
      if (!account) throw new Error('no account')

      setWait(true)

      // metadata
      const name = `${country}-${year}-${ch}`
      const attributes = setAttributes({ country, year, ch })
      const metadata = setMetadata(name, account, attributes)
      console.log(metadata)

      const dataURI = await d3ToPng('#SVG-BOX', name, {
        scale: 1,
        format: 'webp',
        download: false,
      })

      // ipfs
      const token = await uploadImage(dataURI, metadata)
      console.log({ ...token })

      // mint
      if (!token?.url) throw new Error('invalid token url')
      const tokenURI = token.url.replace('ipfs://', '')
      const config = await prepareSafeMint(account, tokenURI)
      const result = await writeContract(config)
      console.log({ result })
    } catch (error) {
      console.error(error)
    }

    setWait(false)
  }

  return (
    <>
      <Head>
        <title>Font NFT</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <AppShell
        header={
          <Header height={{ base: 72 }} p="md">
            <Group position="apart" spacing="xs">
              <Text>Font NFT</Text>
              <ConnectButton />
            </Group>
          </Header>
        }
        navbar={
          <Navbar width={{ base: 400 }}>
            <ScrollArea p={20}>
              <ToolStack />
              <Box sx={{ height: 120 }} />
              <Group
                grow
                sx={{
                  position: 'fixed',
                  bottom: 0,
                  zIndex: 10,
                  left: 10,
                  width: 380,
                  padding: 20,
                  backgroundColor: 'white',
                }}
              >
                <Button
                  size="lg"
                  variant="outline"
                  color="dark"
                  radius="md"
                  onClick={() => setOpen(true)}
                >
                  Preview
                </Button>
                <Button
                  size="lg"
                  variant="outline"
                  color="dark"
                  radius="md"
                  onClick={() => handleMint()}
                  disabled={wait}
                >
                  Mint
                </Button>
              </Group>
            </ScrollArea>
          </Navbar>
        }
        padding={20}
        styles={{
          body: { background: bgColor },
        }}
      >
        <Center h="100%">
          <AspectRatio
            ratio={1}
            sx={{
              width: '100%',
              maxWidth: `calc(100vh - 120px)`,
            }}
          >
            <SvgItem />
          </AspectRatio>
        </Center>

        <Modal
          centered
          opened={open}
          onClose={() => setOpen(false)}
          radius="lg"
          title={
            <Title order={2} className="absolute-horizontal">
              Preview
            </Title>
          }
        >
          <Box sx={{ margin: '32px 0 16px' }}>
            <AspectRatio
              ratio={1}
              sx={{
                width: '100%',
                borderRadius: 16,
                overflow: 'hidden',
              }}
            >
              <SvgItem />
            </AspectRatio>
          </Box>

          <Box sx={{ textAlign: 'center' }}>
            <Button size="lg" variant="outline" color="dark" radius="md" px={64}>
              Mint
            </Button>
          </Box>
        </Modal>

        {/* for d3 */}
        <Box
          sx={{
            position: 'fixed',
            top: 0,
            left: 0,
            zIndex: -1,
            width: 1200,
            height: 1200,
            opacity: 0,
            pointerEvents: 'none',
          }}
        >
          <SvgItem uid="SVG-BOX" />
        </Box>
      </AppShell>
    </>
  )
}

export default Home

export async function getStaticProps() {
  return {
    props: {},
    // Next.js will attempt to re-generate the page:
    // - When a request comes in
    // - At most once every 10 seconds
    revalidate: 60 * 60 * 24, // In seconds
  }
}
