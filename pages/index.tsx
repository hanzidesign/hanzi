import type { NextPage } from 'next'
import _ from 'lodash'
import Head from 'next/head'
import useAccount from 'hooks/useAccount'
import useQueue from 'hooks/useQueue'
import useNft from 'hooks/useNft'
import useChain from 'hooks/useChain'
import { useAppSelector } from 'store'
import { useDisclosure } from '@mantine/hooks'
import { AppShell, Navbar, Header, Text, Modal } from '@mantine/core'
import { Group, Button, Box, Title, Indicator } from '@mantine/core'
import { ScrollArea, AspectRatio, Center } from '@mantine/core'
import { modals } from '@mantine/modals'
import { ConnectButton } from '@rainbow-me/rainbowkit'
import ToolStack from 'components/ToolStack'
import Preview from 'components/Preview'
import Queue from 'components/Queue'
import SvgItem from 'components/SvgItem'
import { Constants } from 'types'

const Home: NextPage<{}> = () => {
  const { bgColor } = useAppSelector((state) => state.editor)
  const { list } = useAppSelector((state) => state.nft)
  const [opened, { open, close }] = useDisclosure(false)
  const unmint = _.compact(_.map(list, (v) => v)).filter((el) => !el.hash)

  // background tasks
  useAccount()
  useQueue()
  useNft()
  useChain()

  const openQueueModal = () => {
    close()
    modals.open({
      title: <span></span>,
      centered: true,
      size: 'xl',
      children: <Queue />,
      styles: {
        content: {
          height: '100%',
        },
      },
    })
  }

  return (
    <>
      <Head>
        <title>Chinese NFT</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <AppShell
        header={
          <Header height={{ base: 72 }} p="md">
            <Group position="apart" spacing="xs">
              <Text>Chinese NFT</Text>
              <ConnectButton />
            </Group>
          </Header>
        }
        navbar={
          <Navbar width={{ base: 400 }}>
            <ScrollArea p={20}>
              <ToolStack />
              <Box sx={{ height: 120 }} />
              <Group
                grow
                sx={{
                  position: 'fixed',
                  bottom: 0,
                  zIndex: 10,
                  left: 10,
                  width: 380,
                  padding: 20,
                  backgroundColor: 'white',
                }}
              >
                <Indicator label={unmint.length} size={24} disabled={unmint.length === 0}>
                  <Button
                    size="lg"
                    variant="outline"
                    color="dark"
                    radius="md"
                    w="100%"
                    onClick={openQueueModal}
                  >
                    Queue
                  </Button>
                </Indicator>
                <Button size="lg" variant="outline" color="dark" radius="md" onClick={open}>
                  Mint
                </Button>
              </Group>
            </ScrollArea>
          </Navbar>
        }
        padding={20}
        styles={{
          body: { background: bgColor },
        }}
      >
        <Center h="100%">
          <AspectRatio
            ratio={1}
            sx={{
              width: '100%',
              maxWidth: `calc(100vh - 120px)`,
            }}
          >
            <SvgItem />
          </AspectRatio>
        </Center>

        {/* for d3 */}
        <Box
          sx={{
            position: 'fixed',
            top: 0,
            left: 0,
            zIndex: -1,
            width: 1200,
            height: 1200,
            opacity: 0,
            pointerEvents: 'none',
          }}
        >
          <SvgItem uid={Constants.svgId} />
        </Box>
      </AppShell>

      <Modal
        opened={opened}
        onClose={close}
        title={
          <span>
            <Title order={2} className="absolute-horizontal">
              Preview
            </Title>
          </span>
        }
        radius="lg"
        centered
      >
        <Preview onBack={close} />
      </Modal>
    </>
  )
}

export default Home

export async function getStaticProps() {
  return {
    props: {},
    // Next.js will attempt to re-generate the page:
    // - When a request comes in
    // - At most once every 10 seconds
    revalidate: 60 * 60 * 24, // In seconds
  }
}
